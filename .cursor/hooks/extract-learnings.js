#!/usr/bin/env bun

/**
 * sessionEnd hook: extract conversation content for learnings processing.
 *
 * Runs when a Composer conversation ends (user closes/archives chat). Reads
 * .taskgraph/config.json for learningMode flag. When enabled, finds the most
 * recent agent transcript and writes a digest to .cursor/pending-learnings.md
 * for the next session's agent to route into formal docs or memory.
 */

const fs = require('fs');
const path = require('path');

const MAX_CONTENT_CHARS = 12000;

let input = '';
process.stdin.setEncoding('utf8');
process.stdin.on('data', (chunk) => {
	input += chunk;
});
process.stdin.on('end', () => {
	try {
		const payload = JSON.parse(input || '{}');
		const root = resolveProjectRoot(payload);
		if (!root) return;

		const configPath = path.join(root, '.taskgraph', 'config.json');
		if (!fs.existsSync(configPath)) return;
		const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
		if (!config.learningMode) return;

		const transcriptsDir = findTranscriptsDir(root);
		if (!transcriptsDir) return;

		const digest = buildDigest(transcriptsDir);
		if (!digest) return;

		const pendingPath = path.join(root, '.cursor', 'pending-learnings.md');
		fs.writeFileSync(pendingPath, digest, 'utf8');
		console.error('[cursor hook] Wrote pending-learnings.md for next session.');
	} catch (err) {
		console.error('[cursor hook] extract-learnings:', err.message);
	}
});

/** Resolve project root from payload or by walking up from cwd for .taskgraph. */
function resolveProjectRoot(payload) {
	const roots = payload.workspace_roots || [];
	if (roots.length > 0) return path.resolve(roots[0]);

	let dir = process.cwd();
	for (let i = 0; i < 10; i++) {
		const configPath = path.join(dir, '.taskgraph', 'config.json');
		if (fs.existsSync(configPath)) return dir;
		const parent = path.dirname(dir);
		if (parent === dir) break;
		dir = parent;
	}
	return null;
}

function findTranscriptsDir(root) {
	const projectSlug = root.replace(/\//g, '-').replace(/^-/, '');
	const candidates = [
		path.join(
			process.env.HOME || '~',
			'.cursor',
			'projects',
			projectSlug,
			'agent-transcripts'
		),
		path.join(
			process.env.HOME || '~',
			'.cursor',
			'projects',
			`Users-${path.basename(process.env.HOME || '')}-${root.split('/').slice(2).join('-')}`,
			'agent-transcripts'
		)
	];
	for (const dir of candidates) {
		if (fs.existsSync(dir)) return dir;
	}
	return null;
}

function buildDigest(transcriptsDir) {
	const dirs = fs
		.readdirSync(transcriptsDir, { withFileTypes: true })
		.filter((d) => d.isDirectory())
		.map((d) => ({
			name: d.name,
			mtime: fs.statSync(path.join(transcriptsDir, d.name)).mtimeMs
		}))
		.sort((a, b) => b.mtime - a.mtime);

	if (dirs.length === 0) return null;

	const latest = dirs[0];
	const jsonlPath = path.join(
		transcriptsDir,
		latest.name,
		`${latest.name}.jsonl`
	);
	if (!fs.existsSync(jsonlPath)) return null;

	const lines = fs.readFileSync(jsonlPath, 'utf8').trim().split('\n');
	const messages = [];
	for (const line of lines) {
		try {
			const entry = JSON.parse(line);
			const role = entry.role || 'unknown';
			const content = extractText(entry);
			if (content) messages.push({ role, content });
		} catch {
			// skip malformed lines
		}
	}

	if (messages.length === 0) return null;

	// Take last N messages, staying under char budget
	let chars = 0;
	const selected = [];
	for (let i = messages.length - 1; i >= 0 && chars < MAX_CONTENT_CHARS; i--) {
		const msg = messages[i];
		const snippet =
			msg.content.length > 2000
				? msg.content.slice(0, 2000) + '\n[...truncated]'
				: msg.content;
		chars += snippet.length;
		selected.unshift({ role: msg.role, content: snippet });
	}

	const timestamp = new Date().toISOString().slice(0, 19).replace('T', ' ');
	const conversationBlock = selected
		.map((m) => `### ${m.role}\n${m.content}`)
		.join('\n\n---\n\n');

	return `# Pending Learnings

> Auto-generated by extract-learnings hook at ${timestamp}.
> The learnings agent should process this file, route findings to formal docs or memory, then delete it.

## Conversation Digest

${conversationBlock}
`;
}

function extractText(entry) {
	const msg = entry.message;
	if (!msg) return '';
	if (typeof msg.content === 'string') return msg.content;
	if (Array.isArray(msg.content)) {
		return msg.content
			.filter((c) => c.type === 'text')
			.map((c) => c.text)
			.join('\n');
	}
	return '';
}
